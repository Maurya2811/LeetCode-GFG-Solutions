class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
    //code
    long long int final=INT_MAX,diff;
    sort(a.begin(),a.end());
    long long int i=0,j=m-1;
    while(j<n){
          diff=a[j]-a[i];
        if( diff < final){
            final=diff;
        }
        i++;
        j++;
    }
    return final;
    }   
};

