//Function to reverse the queue.
class Solution
{
    public:
    void reverse(queue<int> &q,int size){
        if(size<=0){
           return ;
        }
        int temp = q.front();
        q.pop();
        reverse(q,size-1);
        q.push(temp);
        return;
    }
    queue<int> rev(queue<int> q)
    {
       int size = q.size();
        reverse(q,size);
        return q;
    }
};


