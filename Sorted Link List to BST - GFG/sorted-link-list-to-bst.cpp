class Solution{
  public:
     
     TNode* convertToBST(LNode* start , LNode* end){
         if(start==end)
         return NULL;
         
         LNode* slow = start;
         LNode* fast = start;
         
         while(fast!=end && fast->next!=end){
             slow= slow->next;
             fast=fast->next->next;
         }
         TNode* head = new TNode(slow->data);
         
         head->left = convertToBST(start,slow);
         head->right = convertToBST(slow->next,end);
         
         return head;
     }
  
    TNode* sortedListToBST(LNode *head) {
        if(head==NULL)
            return NULL;
            
        return convertToBST(head,NULL);    
    }
};

